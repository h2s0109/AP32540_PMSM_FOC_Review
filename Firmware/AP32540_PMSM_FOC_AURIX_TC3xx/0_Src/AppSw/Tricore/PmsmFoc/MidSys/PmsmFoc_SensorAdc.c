void PmsmFoc_SensorAdc_init(PmsmFoc_SensorAdc *curSense, PmsmFoc_SensorAdc_Config *cfg)
{
	curSense->gain= cfg->gain;
	curSense->limits.max= cfg->maxLimit;
	curSense->limits.min= cfg->minLimit;
	curSense->limits.object= NULL_PTR;
	curSense->limits.onOutOfRange= NULL_PTR;
	curSense->limits.status= PmsmFoc_SensorAdc_LimitsStatus_ok;
	curSense->offset= cfg->offset;
	curSense->rawvalue= 0U;
	curSense->value= 0.0;
}
