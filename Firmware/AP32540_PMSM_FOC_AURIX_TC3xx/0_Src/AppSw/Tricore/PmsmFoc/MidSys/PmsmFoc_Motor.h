 typedef struct
{
    boolean	winding;		/**< \brief Winding type: 0 - Wye, 1 - Delta */
    boolean rotorSailency;  /**< \brief Rotor sailency: 0 - Round, 1 - Sailent */
	uint8 polePairs;		/**< \brief Number of pole pairs */
    float32 rs;				/**< \brief Stator resistance per phase in Ohm */
    float32 ms;				/**< \brief Stator mutual inductance in H */
    float32 lm;				/**< \brief Stator inductance fluctuation in H */
    float32 ls;				/**< \brief Stator self-inductance per phase in H */
    float32 l0;				/**< \brief Stator zero-sequence inductance in H */
    float32 ld;				/**< \brief Stator d-axis inductance in H */
    float32 lq;				/**< \brief Stator q-axis inductance in H */
    float32 ldq;			/**< \brief Stator dq-axis inductance in H */
    float32 ke;				/**< \brief Back EMF constant in V.s/rad */
    float32 psiM;			/**< \brief Permanent magnet flux linkage in Wb */
    float32 tc;				/**< \brief Torque constant in N.m/A */
    float32 jm;				/**< \brief Total mechanical inertia in kg.m^2 */
    float32 bm;				/**< \brief Viscous friction coeficient in N.m.s */

    float32	iNom;			/**< \brief Nominal phase current in A */
    float32 iMax;			/**< \brief Maximal phase current in A */
    float32	uNom;			/**< \brief Nominal phase voltage in V */
    float32	pNom;			/**< \brief Nominal power in W */
    float32	pMax;			/**< \brief Maximal power in W */
    float32 nNom;			/**< \brief Nominal speed in rpm */
    float32 nMax;			/**< \brief Maximal speed in rpm */
} MotorParameters;
