static void PmsmFoc_DcLinkVoltageSense_init(DcLinkVoltageSense * const dcLinkVoltageSense)
{
	dcLinkVoltageSense->input.gain = USER_INVERTER_DCLINVOLTAGESENSE_GAIN;
	dcLinkVoltageSense->input.offset = USER_INVERTER_DCLINVOLTAGESENSE_GAIN;
	dcLinkVoltageSense->input.limits.object = NULL_PTR;
	dcLinkVoltageSense->input.limits.onOutOfRange = NULL_PTR;
	dcLinkVoltageSense->input.limits.status= PmsmFoc_SensorAdc_LimitsStatus_ok;
	dcLinkVoltageSense->input.rawvalue= 0U;
	dcLinkVoltageSense->input.value= 0.0;
}

float32 PmsmFoc_DcLinkVoltageSense_updateAnalogInput(PmsmFoc_SensorAdc * const dcLinkVoltageSense)
{
	float32 value = 1.0f;
	uint32 rawValue = 1;

	rawValue = IfxEvadc_Adc_getResult(&dcLinkVoltageSense->adcChannel).B.RESULT;

	/* Scale Input */
	value = (rawValue * dcLinkVoltageSense->gain + dcLinkVoltageSense->offset);

	/* Check limits */
	// value = PhaseCurSense_limitsExecute(&dcLinkVoltageSense->limits, value);
	/* Store value */
	dcLinkVoltageSense->rawvalue = rawValue;
	dcLinkVoltageSense->value = value;
	return value;
}


void PmsmFoc_DcLinkVoltageSense_getValue(DcLinkVoltageSense * const dcLinkVoltageSense)
{
	PmsmFoc_DcLinkVoltageSense_updateAnalogInput(&dcLinkVoltageSense->input);
}

void PmsmFoc_DcLinkVoltageSense_resetCalibrationStatus(DcLinkVoltageSense* const dcLinkVoltageSense)
{
	PmsmFoc_DcLinkVoltageSense_init(dcLinkVoltageSense);
}

