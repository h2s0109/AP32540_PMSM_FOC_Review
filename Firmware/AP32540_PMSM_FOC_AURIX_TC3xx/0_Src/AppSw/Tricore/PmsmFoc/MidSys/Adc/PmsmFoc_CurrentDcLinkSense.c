static boolean PmsmFoc_CurrentDCLinkSenseHs_init(HighSideCurrentSense *highSideCurrentSense, const HighSideCurrentSense_Config* config);
static float32 PmsmFoc_CurrentDCLinkSenseHs_limitsExecute(PmsmFoc_SensorAdc_Limits* limits, float32 in);

static boolean PmsmFoc_CurrentDCLinkSenseHs_init(HighSideCurrentSense *highSideCurrentSense, const HighSideCurrentSense_Config* config)
{
	boolean result = TRUE;

	highSideCurrentSense->input.gain= USER_INVERTER_HS_DCLINK_CUR_SENSE_GAIN;
	highSideCurrentSense->input.offset= USER_INVERTER_HS_DCLINK_CUR_SENSE_OFFSET;
	highSideCurrentSense->input.limits.max= USER_INVERTER_HS_DCLINK_CUR_SENSE_I_MAX;
	highSideCurrentSense->input.limits.min= USER_INVERTER_HS_DCLINK_CUR_SENSE_I_MIN;
	highSideCurrentSense->input.limits.object= NULL_PTR;
	highSideCurrentSense->input.limits.onOutOfRange= NULL_PTR;
	highSideCurrentSense->input.limits.status= PmsmFoc_SensorAdc_LimitsStatus_ok;
	highSideCurrentSense->input.rawvalue= 0U;
	highSideCurrentSense->input.value= 0.0;

	highSideCurrentSense->pins.gainSelelct0 = config->pins.gainSelelct0;
	highSideCurrentSense->pins.gainSelelct1 = config->pins.gainSelelct1;

    IfxPort_setPinMode(highSideCurrentSense->pins.gainSelelct0->port, highSideCurrentSense->pins.gainSelelct0->pinIndex, IfxPort_Mode_outputPushPullGeneral);
    IfxPort_setPinMode(highSideCurrentSense->pins.gainSelelct1->port, highSideCurrentSense->pins.gainSelelct1->pinIndex, IfxPort_Mode_outputPushPullGeneral);

    return result;
}

