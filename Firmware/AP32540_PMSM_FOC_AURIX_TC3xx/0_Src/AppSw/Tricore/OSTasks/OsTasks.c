volatile uint32 ulIdleCycleCount = 0UL;
void vApplicationIdleHook( void )
{
	/* This hook function does nothing but increment a counter. */
	ulIdleCycleCount++;
	{
		/******************************************************************
		 *                        IDLE Task START                         *
		 ******************************************************************/
		#if(ONE_EYEMODE == ENABLED)
			OneEye_processDataStream();
		#endif /* End of ONE_EYEMODE*/
		/******************************************************************
		 *                         IDLE Task END                          *
		 ******************************************************************/
	}
}

